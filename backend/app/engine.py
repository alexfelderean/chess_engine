def lazy_eval(board: chess.Board) -> int:
    black_material = 0
    white_material = 0
    
    for square in chess.SQUARES:
        piece = board.piece_at(square)
        
        if not piece:
            continue
        elif piece.color == chess.WHITE:
            white_material += piece_values[piece.piece_type] + piece_position_rewards_white[piece.piece_type][square]
        else:
            black_material += piece_values[piece.piece_type] + piece_position_rewards_black[piece.piece_type][square]
            
    return white_material - black_material

def max_value(board: chess.Board, alpha: int, beta: int, ply_depth: int, max_ply_depth: int) -> int: 
    if(board.is_game_over()):
        res = board.result()
        if res == "1-0":
            return float('inf')
        elif res == "0-1":
            return -float('inf')
        else:
            return 0
        
    if(ply_depth == max_ply_depth):
        return lazy_eval(board)
    
    eval = -float('inf')
    for move in board.legal_moves:
        board.push(move)
        eval = max(eval, min_value(board, alpha, beta, ply_depth + 1, max_ply_depth))
        board.pop()
        if(eval >= beta):
            return eval
        alpha = max(alpha, eval)
    
    return eval

def min_value(board: chess.Board, alpha: int, beta: int, ply_depth: int, max_ply_depth: int) -> int: 
    if(board.is_game_over()):
        res = board.result()
        if res == "1-0":
            return float('inf')
        elif res == "0-1":
            return -float('inf')
        else:
            return 0
        
    if(ply_depth == max_ply_depth):
        return lazy_eval(board)
    
    eval = float('inf')
    for move in board.legal_moves:
        board.push(move)
        eval = min(eval, max_value(board, alpha, beta, ply_depth + 1, max_ply_depth))
        board.pop()
        if(eval <= alpha):
            return eval # prune / early stopping
        beta = min(beta, eval)
        
    return eval


def alpha_beta_minimax(board: chess.Board, ply_depth: int, max_ply_depth: int) -> chess.Move:
    # should always be playing from black's perspective for now
    moves = list(board.legal_moves)
    best_move = None

    alpha = -float('inf')
    beta = float('inf')
    if(board.turn == chess.WHITE):
        best_eval = -float('inf')
        for move in moves:
            board.push(move)
            eval = min_value(board, alpha, beta, ply_depth + 1, max_ply_depth)
            board.pop()
            if(eval > best_eval):
                best_eval = eval
                best_move = move
                alpha = eval

    else:
        best_eval = float('inf')
        for move in moves:
            board.push(move)
            eval = max_value(board, alpha, beta, ply_depth + 1, max_ply_depth)
            board.pop()
            if(eval < best_eval):
                best_eval = eval
                best_move = move
                beta = eval

    return best_move
